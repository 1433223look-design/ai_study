  toggleTopicTag(el) {
    if (this._isScrolling) return;
    const topic = el.dataset.topic;
    const row = document.getElementById('topicTagRow');
    const randomTag = row.querySelector('[data-topic="__random__"]');

    if (topic === '__random__') {
      // 点随机 → 取消所有其他选中，选中随机
      this.selectedTopics.clear();
      row.querySelectorAll('.topic-tag').forEach(t => t.classList.remove('selected'));
      row.querySelectorAll('.topic-chapter-header').forEach(h => h.classList.remove('chapter-active'));
      el.classList.add('selected');
    } else {
      // 点具体知识点 → 取消随机
      randomTag?.classList.remove('selected');

      if (this.selectedTopics.has(topic)) {
        this.selectedTopics.delete(topic);
        el.classList.remove('selected');
      } else {
        this.selectedTopics.add(topic);
        el.classList.add('selected');
      }

      // 更新所属章节标题高亮
      const group = el.closest('.topic-chapter-group');
      if (group) {
        const header = group.querySelector('.topic-chapter-header');
        const tags = group.querySelectorAll('.topic-tag');
        const anySelected = [...tags].some(t => t.classList.contains('selected'));
        header?.classList.toggle('chapter-active', anySelected);
      }

      // 如果没有任何知识点被选中，重新选中随机
      if (this.selectedTopics.size === 0) {
        randomTag?.classList.add('selected');
      }
    }

    this.updateTopicInfo();
  }

